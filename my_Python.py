#datatypes
#In Python, data types represent the kind of values that can be stored and manipulated in a program. Here's a summary of some of the main data types in Python:
#Numeric Types: int: Integer values without any decimal point.float: Floating-point values, representing real numbers with decimal points.
#complex: Numbers with a real and imaginary part, represented as a + bj, where a and b are real numbers and j is the imaginary unit.
#Sequence Types:str: Immutable sequence of characters, representing text data.list: Mutable sequence of elements, allowing heterogeneous data types and dynamic resizing.
#tuple: Immutable sequence of elements, similar to lists but cannot be modified after creation.
#Mapping Type:dict: Collection of key-value pairs, where each key maps to a value. Keys must be unique and immutable.
#Set Types:set: Unordered collection of unique elements, useful for mathematical operations like union, intersection, etc. frozenset: Immutable version of set, which cannot be modified after creation.
#Boolean Type:bool: Represents boolean values True and False, used for logical operations and control flow.
