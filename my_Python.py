# Its development began in December 1989, and Guido released the first version, Python 0.9.0, 
#in February 1991. The language was designed with a focus on simplicity and readability, aiming to make programming more accessible and enjoyable. 
#Guido named it after the British comedy group Monty Python, not the snake, to add a bit of humor to the project. 
#Python's syntax emphasizes code readability, with the use of significant whitespace rather than curly braces or keywords, making it easy to understand and learn for beginners.
# Over the years, Python has evolved into one of the most popular and versatile programming languages, used in various fields such as web development, data science, artificial intelligence, and more.

#variables

# In Python, variables are used to store data values. They are containers for storing information that can be referenced and manipulated in a program.
# Key points about variables in Python include:Dynamic Typing: Python uses dynamic typing, meaning you don't need to declare the type of a variable explicitly. The interpreter automatically determines the type based on the assigned value.
#Variable Naming: Variable names can contain letters, numbers, and underscores but cannot start with a number. They are case-sensitive.
#Assignment: Variables are assigned using the '=' operator. For example, x = 5 assigns the value 5 to the variable x.
#Data Types: Variables in Python can hold different types of data, including integers, floats, strings, lists, tuples, dictionaries, and more.
